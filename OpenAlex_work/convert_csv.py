"""
Конвертирует vuz_contacts.csv в формат, нужный для fetch_university_counts.py
(university_name, country_code).

Создаёт два файла:
  - universities_full.csv  — все действующие вузы
  - universities_test.csv  — небольшая выборка для проверки работоспособности
"""
import csv
import re

INPUT = "vuz_contacts_test.csv"
OUTPUT_FULL = "universities_full.csv"
OUTPUT_TEST = "universities_test.csv"

# Префиксы, которые мешают поиску в OpenAlex — вырезаем.
PREFIX_PATTERNS = [
    r"федеральное государственное автономное образовательное учреждение высшего (профессионального )?образования\s*",
    r"федеральное государственное бюджетное образовательное учреждение высшего (профессионального )?образования\s*",
    r"федеральное государственное казенное образовательное учреждение высшего (профессионального )?образования\s*",
    r"федеральное государственное бюджетное военное образовательное учреждение высшего образования\s*",
    r"федеральное государственное казенное военное образовательное учреждение высшего образования\s*",
    r"федеральное государственное автономное учреждение высшего образования\s*",
    r"федеральное государственное бюджетное учреждение (науки\s+)?\s*",
    r"государственное бюджетное образовательное учреждение высшего (профессионального )?образования\s*",
    r"государственное автономное образовательное учреждение высшего образования\s*",
    r"автономная некоммерческая организация высшего (профессионального )?образования\s*",
    r"негосударственное (некоммерческое )?образовательное (частное )?учреждение высшего (профессионального )?образования\s*",
    r"негосударственное образовательное учреждение высшего (профессионального )?образования\s*",
    r"частное образовательное учреждение высшего (профессионального )?образования\s*",
    r"частное учреждение высшего образования\s*",
    r"образовательное частное учреждение высшего образования\s*",
    r"образовательное учреждение высшего образования\s*",
]


def clean_name(full_name: str) -> str:
    """Приводит официальное название к короткой форме, близкой к тому,
    как вуз записан в OpenAlex (без юр. префикса, без 'национальный
    исследовательский', без 'имени ...' и т.п.)."""
    name = full_name.strip()

    # 1. Юридический префикс (регистронезависимо)
    for pat in PREFIX_PATTERNS:
        new = re.sub(pat, "", name, count=1, flags=re.IGNORECASE)
        if new != name:
            name = new
            break
    name = name.strip()

    # 2. Убираем «ёлочки» вокруг всего названия целиком
    m = re.match(r'^[«"](.+)[»"]$', name)
    if m:
        name = m.group(1).strip()

    # 3. Убираем «шумовые» добавки (регистронезависимо, по одному проходу)
    noise_patterns = [
        r"\s*имени\s+[^,»\"]+$",                    # "... имени М.В.Ломоносова"
        r"\s*им\.\s*[^,»\"]+$",                     # "... им. Ленина"
        r"\s+национальный\s+исследовательский\s+", # НГУ, ТГУ, ВШЭ
        r"\s+\(приволжский\)\s*",                   # Казанский (Приволжский) федеральный
        r"\s+федеральный\s+",                       # УрФУ (оставим, но уберём окружение)
        r"\s+министерства\s+[^,»\"]+$",             # "... Министерства обороны"
    ]
    # Срезаем только "имени ..." и аналогичные постфиксы — остальное опасно
    name = re.sub(noise_patterns[0], "", name, flags=re.IGNORECASE).strip()
    name = re.sub(noise_patterns[1], "", name, flags=re.IGNORECASE).strip()
    name = re.sub(noise_patterns[5], "", name, flags=re.IGNORECASE).strip()

    # "национальный исследовательский" — удаляем, оно не в альтернативах OpenAlex
    name = re.sub(r"\bнациональный\s+исследовательский\s+", "", name, flags=re.IGNORECASE)
    # "(Приволжский)" и подобные уточнения в скобках
    name = re.sub(r"\s*\([^)]{1,40}\)\s*", " ", name)

    # Сжимаем пробелы
    name = re.sub(r"\s+", " ", name).strip(" ,-—–")
    return name or full_name.strip()


def main() -> None:
    with open(INPUT, encoding="utf-8-sig", newline="") as f:
        rows = list(csv.DictReader(f))

    active = [r for r in rows if r.get("Статус лицензии", "").strip() == "Действующее"]
    print(f"Всего записей: {len(rows)}, действующих: {len(active)}")

    # --- полный файл ---
    with open(OUTPUT_FULL, "w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(["university_name", "country_code"])
        for r in active:
            full = r["Название (полное)"].strip()
            if not full:
                continue
            w.writerow([clean_name(full), "RU"])
    print(f"Создан {OUTPUT_FULL} — {len(active)} университетов")

    # --- тестовый файл: 10 крупных и узнаваемых (ищем во ВСЁМ датасете,
    # так как ряд крупных вузов помечен как "Недействующее" из-за
    # переоформления лицензий, но в OpenAlex они есть).
    # (обязательная подстрока, запрещённая подстрока)
    keywords = [
        ("«московский государственный университет имени м.в.ломоносова»", None),
        ("санкт-петербургский государственный университет»", None),  # основной СПбГУ
        ("новосибирский национальный исследовательский", None),
        ("казанский (приволжский) федеральный", None),
        ("национальный исследовательский томский государственный", None),
        ("самарский национальный исследовательский", None),
        ("уральский федеральный университет", None),
        ("российский университет дружбы народов", None),
        ("«мифи»", "филиал"),   # не филиал, а сам университет
        ("«высшая школа экономики»", None),
    ]

    picked = []
    used = set()
    for kw_in, kw_not in keywords:
        for r in rows:  # по всему датасету
            full = r["Название (полное)"].strip().lower()
            if kw_in in full and (kw_not is None or kw_not not in full) \
                    and r["Название (полное)"] not in used:
                picked.append(r)
                used.add(r["Название (полное)"])
                break

    # Если что-то не нашлось — добираем первыми действующими
    if len(picked) < 10:
        for r in active:
            if r["Название (полное)"] not in used:
                picked.append(r)
                used.add(r["Название (полное)"])
            if len(picked) >= 10:
                break

    with open(OUTPUT_TEST, "w", encoding="utf-8", newline="") as f:
        w = csv.writer(f)
        w.writerow(["university_name", "country_code"])
        for r in picked:
            w.writerow([clean_name(r["Название (полное)"]), "RU"])
    print(f"Создан {OUTPUT_TEST} — {len(picked)} университетов для теста")
    print("\nСодержимое тестового файла:")
    for r in picked:
        print("  -", clean_name(r["Название (полное)"]))


if __name__ == "__main__":
    main()
